	prestataire en fin de prestation. Il appartient au client de vérifier l’exactitude de la commande
	et de signaler immédiatement toute erreur. Le prestataire se réserve le droit d’annuler ou de
	refuser toute demande d’un Client pour des raisons de litiges financiers ou de positionnement
	déontologique qui ne lui permettrait pas d’assurer la prestation.
